class KeyNeuralNet(KeyClassifier):
    def __init__(self, net, solution):
        self.net = net
        self.solution = solution

    def keySelector(self, distance, speed, obType):
        tipo = 0
        if isinstance(obType, Bird):
            tipo = 1
        
        data_in = np.array([[distance,speed,tipo]])
        prediction = pygad.kerasga.predict(model=self.net,solution=self.solution,data=data_in)
        action = np.argmax(prediction[0])
        if action == 0:
            return "K_NO"
        elif action == 1:
            return "K_DOWN"
        elif action == 2:
            return "K_UP"        
        
        return "K_NO"

    def updateState(self, state):
        self.state = state

def playGame():
    global game_speed, x_pos_bg, y_pos_bg, points, obstacles
    run = True
    clock = pygame.time.Clock()
    player = Dinosaur()
    cloud = Cloud()
    game_speed = 20
    x_pos_bg = 0
    y_pos_bg = 380
    points = 0
    font = pygame.font.Font('freesansbold.ttf', 20)
    obstacles = []
    death_count = 0

    def score():
        global points, game_speed
        points += 1
        if points % 100 == 0:
            game_speed += 1

        text = font.render("Points: " + str(points), True, (0, 0, 0))
        textRect = text.get_rect()
        textRect.center = (1000, 40)
        SCREEN.blit(text, textRect)

    def background():
        global x_pos_bg, y_pos_bg
        image_width = BG.get_width()
        SCREEN.blit(BG, (x_pos_bg, y_pos_bg))
        SCREEN.blit(BG, (image_width + x_pos_bg, y_pos_bg))
        if x_pos_bg <= -image_width:
            SCREEN.blit(BG, (image_width + x_pos_bg, y_pos_bg))
            x_pos_bg = 0
        x_pos_bg -= game_speed

    while run:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                run = False

        SCREEN.fill((255, 255, 255))

        distance = 1500
        obType = 2
        if len(obstacles) != 0:
           xy = obstacles[0].getXY()
           distance = xy[0]
           obType = obstacles[0]

        userInput = aiPlayer.keySelector(distance, game_speed, obType)
         
        player.draw(SCREEN)
        player.update(userInput)

        if len(obstacles) == 0:
            if random.randint(0, 2) == 0:
                obstacles.append(SmallCactus(SMALL_CACTUS))
            elif random.randint(0, 2) == 1:
                obstacles.append(LargeCactus(LARGE_CACTUS))
            elif random.randint(0, 2) == 2:
                obstacles.append(Bird(BIRD))

        for obstacle in obstacles:
            obstacle.draw(SCREEN)
            obstacle.update()
            if player.dino_rect.colliderect(obstacle.rect):
                pygame.time.delay(2000)
                death_count += 1
                return points

        background()

        cloud.draw(SCREEN)
        cloud.update()

        score()

        clock.tick(30)
        pygame.display.update()



def manyPlaysResults(rounds):

  results = []
  for round in range (rounds):
    results += [playGame()]
  npResults = np.asarray(results)
  return npResults.mean()

